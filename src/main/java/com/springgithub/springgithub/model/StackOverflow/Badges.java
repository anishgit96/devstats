package com.springgithub.springgithub.model.StackOverflow;

public class Badges {
    private int badgeId;
    private String rank;
    private String name;
    private String awardCount;

    public int getBadgeId() {
        return badgeId;
    }

    public String getRank() {
        return rank;
    }

    public String getName() {
        return name;
    }

    public String getAwardCount() {
        return awardCount;
    }
}
